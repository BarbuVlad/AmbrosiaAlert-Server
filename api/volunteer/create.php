<?php
  // Send headers with HTTP
  header('Access-Control-Allow-Origin: *'); // can be read by anyone
  header('Content-Type: application/json'); // returns a json format file
  header("Access-Control-Allow-Methods: POST");
  header("Access-Control-Max-Age: 3600");
  header('Access-Control-Allow-Headers: Access-Control-Allow-Headers,Content-Type,Access-Control-Allow-Methods, Authorization, X-Requested-With');
  // json header can be passed for volunteer data

  include_once '../../config/Database.php';
  include_once '../../objects/Volunteer.php';
  // Instantiate DB & connect //cine face request?? un volunteer minimal, ...
  $database = new Database();
  $db = $database->connect();

  // Instantiate a table object
  $volunteer = new Volunteer($db);

  //get data from request
  $data = json_decode(file_get_contents("php://input"), true);//data from body of the request
  //pass data to volunteer
  $volunteer->phone = $data['phone'];
  $volunteer->email = $data['email'];

  //hashing the pasword
  $volunteer->password = password_hash($data['password'], PASSWORD_BCRYPT);

  $volunteer->first_name = isset($data['first_name']) ? $data['first_name'] : 'null';
  $volunteer->last_name = isset($data['last_name']) ? $data['last_name'] : 'null';
  $volunteer->address = isset($data['address']) ? $data['address'] : 'null';
  //$volunteer->uid autogenerated PK

  if($volunteer->create()){
    http_response_code(200);
    echo json_encode(array('message' => 'volunteer created'));
  } else {
    http_response_code(503);
    echo json_encode(array('message' => 'ERROR occurred. volunteer NOT created'));
  }
?>
